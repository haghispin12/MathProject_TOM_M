package com.tom.mathproject_tom_m;

public class User {
    private String Name;
    private int Score;
    private int Rate;

    public int getScore() {
        return Score;
    }

    public int getRate() {
        return Rate;
    }

    public String getName() {
        return Name;
    }

    public void setName(String name) {
        Name = name;
    }

    public void setScore(int score) {
        Score = score;
    }

    public void setRate(int rate) {
        Rate = rate;
    }
}
