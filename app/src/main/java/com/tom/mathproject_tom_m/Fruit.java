package com.tom.mathproject_tom_m;

public class Fruit {
    private String name;
    private int drawable;
    public Fruit() {
        this.drawable = drawable;
        this.name = name;
    }
    public String getName() {
        return name;
    }
    public int getDrawable() {
        return drawable;
    }
    public void setDrawable(int drawable) {
        this.drawable = drawable;
    }
    public void setName(String name) {
        this.name = name;
    }

    }

